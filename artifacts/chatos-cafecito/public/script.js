const RTL_LANGS = new Set(["ar"]);
const STORAGE_KEY = "chatos.lang";

function detectInitialLang() {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored && translations[stored]) return stored;

  const nav = (navigator.language || "es").slice(0, 2).toLowerCase();
  return translations[nav] ? nav : "es";
}

function applyLanguage(lang) {
  const dict = translations[lang];
  if (!dict) return;

  document.documentElement.lang = lang;
  document.documentElement.dir = RTL_LANGS.has(lang) ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = dict[key];
    if (value === undefined) return;
    if (value.includes("<br>")) {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });

  localStorage.setItem(STORAGE_KEY, lang);
}

function buildLanguageOptions(selectEl) {
  const fragment = document.createDocumentFragment();
  Object.entries(translations).forEach(([code, dict]) => {
    const opt = document.createElement("option");
    opt.value = code;
    opt.textContent = dict.label || code.toUpperCase();
    fragment.appendChild(opt);
  });
  selectEl.appendChild(fragment);
}

function initSmoothScrollOffset() {
  document.querySelectorAll('a[href^="#"]').forEach((link) => {
    link.addEventListener("click", (e) => {
      const targetId = link.getAttribute("href");
      if (targetId.length <= 1) return;
      const target = document.querySelector(targetId);
      if (!target) return;
      e.preventDefault();
      const headerOffset = 70;
      const top = target.getBoundingClientRect().top + window.pageYOffset - headerOffset;
      window.scrollTo({ top, behavior: "smooth" });
    });
  });
}

function initRevealOnScroll() {
  if (!("IntersectionObserver" in window)) return;
  const targets = document.querySelectorAll(".menu-card, .stat, .hours-table, .map-wrapper");
  targets.forEach((el) => {
    el.style.opacity = "0";
    el.style.transform = "translateY(18px)";
    el.style.transition = "opacity .6s ease, transform .6s ease";
  });

  const io = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.style.opacity = "1";
        entry.target.style.transform = "translateY(0)";
        io.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  targets.forEach((el) => io.observe(el));
}

function init() {
  const select = document.getElementById("langSelect");
  buildLanguageOptions(select);

  const initialLang = detectInitialLang();
  select.value = initialLang;
  applyLanguage(initialLang);

  select.addEventListener("change", (e) => applyLanguage(e.target.value));

  initSmoothScrollOffset();
  initRevealOnScroll();
}

document.addEventListener("DOMContentLoaded", init);
